/** Listă plată, în ordine canonică, dintr-o mapare de cărți. */
export function bookList(books) {
  return Object.entries(books)
    .map(([abbrev, meta]) => ({ abbrev, ...meta }))
    .sort((a, b) => a.order - b.order);
}
